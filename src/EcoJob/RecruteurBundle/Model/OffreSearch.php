<?php

namespace EcoJob\RecruteurBundle\Model;


class OffreSearch
{
    private $contenu;

    public function setContenu($contenu)
    {
        $this->contenu = $contenu;

        return $this;
    }

    /**
     * Get contenu
     *
     * @return string
     */
    public function getContenu()
    {
        return $this->contenu;
    }
}

